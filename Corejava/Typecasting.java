class Typecasting{
    public static void main(String[] args) {
        float f=5.6f;
        int x=(int)f;
        System.out.println(x);

        int a=257; //divide no. by byte range which is 256
        byte k=(byte)a;
        System.out.println(k);
        int b=12; //divide no. by byte range which is 256
        byte j=(byte)b;
        System.out.println(j);
    }
}